from google.appengine.ext import ndb

class ScoutingData(ndb.Model):
    scoutingKey = ndb.StringProperty()
    teamNumber = ndb.IntegerProperty()
    eventName = ndb.StringProperty()
    student = ndb.StringProperty()
    crossedBaseline = ndb.BooleanProperty()
    hoppersDumped = ndb.IntegerProperty()
    gearScoredRightAuto = ndb.BooleanProperty() 
    gearScoredCenterAuto = ndb.BooleanProperty() 
    gearScoredLeftAuto = ndb.BooleanProperty() 
    gearsScored = ndb.IntegerProperty()
    gearsDelivered = ndb.IntegerProperty()
    gearsCollectedFromFloor = ndb.IntegerProperty()
    gearsFromHuman = ndb.IntegerProperty()
    scoresHighAuto = ndb.StringProperty()
    scoresLowAuto = ndb.StringProperty()
    ballsInHighCycle = ndb.IntegerProperty()
    ballsFromHuman = ndb.IntegerProperty()
    ballsFromHopper = ndb.IntegerProperty()
    ballsFromFloor = ndb.IntegerProperty()
    fuelInLowCycle = ndb.IntegerProperty()
    numberOfLowCycles = ndb.IntegerProperty()
    highGoalAccuracy = ndb.StringProperty()
    didScale = ndb.BooleanProperty() 
    whereScaled = ndb.StringProperty()
    matchScouted = ndb.BooleanProperty()